#iterating 5 times using a list 
for _ in [5,4,3,2,1]:
    print("meow")

#iterating 5 times using range(n) function which returns a list from 0 to n-1
for _ in range(5):
    print("bow")

#iterating using variables from a list
friends = ["varun","varshini","pratyush","shriyan"]
for friend in friends:
    print(friend)